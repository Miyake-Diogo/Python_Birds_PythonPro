# Containers e Iteração
# Loop While

# loop While (enquanto)
# seja o exemplo
nome = 'Diogo'

# suponha que desejamos imprimir cada caractere
# de forma automatica

# coloque o indice como zero
i = 0
while (i < len(nome)): # enquanto i < tamanho dos caracteres
    print(nome[i]) # Improme o caractere de tamanho i
    i += 1 # incrementa no i
    # após isso retorna e vai imprimindo os itens.
    # quando a condicional no while for falsa, termina o loop




